export function getCells(type, rotation) {
  return SHAPES[type][((rotation % 4) + 4) % 4];
}

// Fisher-Yates 7-bag. rng는 [0,1) 난수 함수(테스트 시 주입 가능).
export function createBag(rng = Math.random) {
  let bag = [];
  function refill() {
    bag = PIECE_TYPES.slice();
    for (let i = bag.length - 1; i > 0; i--) {
      const j = Math.floor(rng() * (i + 1));
      [bag[i], bag[j]] = [bag[j], bag[i]];
    }
  }
  return {
    next() {
      if (bag.length === 0) refill();
      return bag.shift();
    },
    peek(count) {
      while (bag.length < count) {
        const more = PIECE_TYPES.slice();
        for (let i = more.length - 1; i > 0; i--) {
          const j = Math.floor(rng() * (i + 1));
          [more[i], more[j]] = [more[j], more[i]];
        }
        bag = bag.concat(more);
      }
      return bag.slice(0, count);
    },
  };
}
